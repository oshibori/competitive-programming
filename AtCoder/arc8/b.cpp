  string kit;
  cin >> kit;
  map<char, int> n, k;
  for (auto c : kit) {
    k[c]++;
  }
  for (auto c : name) {
    if (not k.count(c)) {
      cout << -1 << endl;
      return 0;
    }
    n[c]++;
  }

  int ans = 0;
  for (auto o : n) {
    chmax(ans, (o.second + k[o.first] - 1) / k[o.first]);
  }
  cout << ans << endl;

  return 0;
